
package all;


public class Generics {
    
    public static void main(String[] args) {
         System.out.printf("%3d#%4s#%4.2f\n", 1234, "Java", 51.6653);
        
         System.out.printf("%8d%8s%8.1f\n", 1234, "Java", 5.63);
 System.out.printf("%-8d%-8s%-8.1f \n", 1234, "Java", 5.63);
    }
}
